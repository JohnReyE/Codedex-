my_favorite_fruits = {"Apple", "Strawberry", "Banana", "Grapes"}
bff_favorite_fruits = {"Mango", "Cherry", "Strawberry", "Grapes"}

union_result = my_favorite_fruits.union(bff_favorite_fruits)
intersection_result = my_favorite_fruits.intersection(bff_favorite_fruits)
difference_result = my_favorite_fruits.difference(bff_favorite_fruits)


print(union_result)
print(intersection_result)
print(difference_result)